print ("My name is")
i = 0
while i < 5:
    print('Jimmy Five Times (' + str(i) + ')')
    i = i + 1
# The code above prints 'My name is' followed by the string 'Jimmy Five Times' five times, with each iteration showing the current index in parentheses. The `while` loop continues as long as `i` is less than 5, incrementing `i` by 1 in each iteration.